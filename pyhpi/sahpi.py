class EnumerationType(type(c_int)):
    def __new__(metacls, name, bases, dict):
        if not "_members_" in dict:
            _members_ = {}
            for key,value in dict.items():
                if not key.startswith("_"):
                    _members_[key] = value
            dict["_members_"] = _members_
        cls = type(c_int).__new__(metacls, name, bases, dict)
        for key,value in cls._members_.items():
            globals()[key] = value
        return cls

    def __contains__(self, value):
        return value in self._members_.values()

    def __repr__(self):
        return "<Enumeration %s>" % self.__name__


class Enumeration(c_int):
    __metaclass__ = EnumerationType
    _members_ = {}
    def __init__(self, value):
        for k,v in self._members_.items():
            if v == value:
                self.name = k
                break
        else:
            raise ValueError("No enumeration member with value %r" % value)
        c_int.__init__(self, value)

    @classmethod
    def from_param(cls, param):
        if isinstance(param, Enumeration):
            if param.__class__ != cls:
                raise ValueError("Cannot mix enumeration members")
            else:
                return param
        else:
            return cls(param)

    def __str__(self):
        return self.name

    def __repr__(self):
        return "<member %s=%d of %r>" % (self.name, self.value, self.__class__)

# XXX this is a hack.
# Because ctypes doesn't support proper alignments of types, we have to insert
# dummy fields where the alignment doesn't match the structures generated by
# the c compiler.
def _get_align(type):
    a = 0

    # handle arrays correctly, use first element
    if (issubclass(type, Array)):
            type = type._type_

    if hasattr(type, '_align_'):
        a = max(a, type._align_)
    else:
        a = alignment(type)

    # structures and unions
    if hasattr(type, '_fields_'):
        for n, t in type._fields_:
            a = max(a, _get_align(t))
    return a

def _aligned(fields):
    """
    Patch the 'fields' sequence for a ctypes Structure by inserting dummy
    fields named '' so that the alignment corresponds to its '_align_' property
    if set or the alignment is retrieved by the alignment() function.
    """
    result = list()
    offset = 0
    for n, t in fields:
        a = _get_align(t)
        #print "align for %s = %d" % (n,a)

        missing = (a - offset % a) % a
        if missing != 0:
            #print "appending %d bytes before %s (offset=%d align=%d)" % (missing, n,
            #        offset, a)
            result.append(('', c_char * missing))
            offset += missing

        #print "%s at offset %d" % (n, size)
        offset += sizeof(t)

        missing = sizeof(t) % a
        if missing != 0:
            #print "appending %d bytes after %s (offset=%d align=%d)" % (missing, n,
            #        offset, a)
            result.append(('', c_char * missing))
            offset += missing

        result.append((n, t))
    return result

