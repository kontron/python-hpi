def _argument_list_to_string(args):
    _args = list()
    for arg in args:
        if arg.__class__.__name__ in ('c_uint', 'c_ulong', 'c_int', 'c_long'):
            _args.append('%d' % int(arg.value))
        else:
            _args.append('0x%08x' % (id(arg),))
    return ', '.join(_args)

def _sahpi_errcheck(result, func, args):
    logger.debug('%s(%s) = %d' % (func.func_name,
        _argument_list_to_string(args), result.value))
    if result.value != SA_OK:
        raise SaHpiError(result.value, SaErrorT(result.value).name)

def _declare_sahpi_function_prototypes():
    if 'HPI_LIBRARY_PATH' in os.environ:
        lib = CDLL(os.environ['HPI_LIBRARY_PATH'])
    else:
        lib = CDLL('libopenhpi.so')
    thismodule = sys.modules[__name__]
    for func_desc in sahpi_functions:
        (name, arg_desc) = func_desc

        arg_types = list()
        params = list()
        for desc in arg_desc:
            if desc[1].startswith('*'):
                arg_types.append(POINTER(desc[0]))
                params.append((3,desc[1][1:]))
            else:
                arg_types.append(desc[0])
                params.append((1,desc[1]))

        prototype = CFUNCTYPE(SaErrorT, *arg_types)
        func = prototype((name, lib), tuple(params))
        func.func_name = name
        func.errcheck = _sahpi_errcheck
        setattr(thismodule, name, func)

